/// <summary>
/// Specifies the encoding options for files.
/// </summary>
public enum EncodingOption
{
    /// <summary>
    /// Indicates that the file should be encoded to base64.
    /// </summary>
    Base64,

    /// <summary>
    /// Indicates no encoding, file remains in plain format.
    /// </summary>
    Plain
}